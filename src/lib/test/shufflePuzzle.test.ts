import { shufflePuzzle } from 'lib/shufflePuzzle'
import { describe, test, expect } from 'vitest'

describe('shufflePuzzle', () => {
  // `output` values are snapshots - with different seeds the output will be different
  const testCases = [
    {
      seed: '1',
      puzzle: '.................................................................................',
      output: '.................................................................................',
    },
    {
      seed: '2',
      puzzle: '123456789........................................................................',
      output: '159248376........................................................................',
    },
    {
      seed: '3',
      puzzle: '111111111222222222333333333444444444555555555666666666777777777888888888999999999',
      output: '666666666555555555444444444222222222111111111333333333777777777999999999888888888',
    },
    {
      seed: '4',
      puzzle: '12.4.6..9...78...3.893..6....782...45...34............2.5...9...9..7...2.4......8',
      output: '78.9.6..5...21...3.153..6....218...94...39............8.4...5...5..2...8.9......1',
    },
    {
      seed: '5',
      puzzle: '..3.2.6..9..3.5..1..18.64....81.29..7.......8..67.82....26.95..8..2.3..9..5.1.3..',
      output: '..1.5.3..6..1.4..8..89.32....98.56..7.......9..37.95....53.64..9..5.1..6..4.8.1..',
    },
  ]

  testCases.forEach(({ puzzle, output }, index) => {
    test(`${puzzle}`, () => {
      const outputPuzzle = shufflePuzzle(puzzle, index.toString())
      expect(outputPuzzle).toBe(output)
    })
  })
})
